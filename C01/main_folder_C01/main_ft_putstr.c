#include <stdio.h>
#include <unistd.h>

void	ft_putstr(char *str);

int	main(void)
{
	ft_putstr("hello");
	write(1, "\n", 1);
	ft_putstr("holla\noutro");
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}
