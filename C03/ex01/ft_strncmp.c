int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	while (n == 0)
	{
		return (0);
	}
	while (*s1 != '\0' && *s2 != '\0' && *s1 == *s2 && n - 1 > 0)
	{
		s1++;
		s2++;
		n--;
	}
	if (*s1 == *s2)
	{
		return (0);
	}
	return (*s1 - *s2);
}
