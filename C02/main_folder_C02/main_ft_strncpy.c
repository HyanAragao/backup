#include <stdio.h>
#include <stdlib.h>
#include <string.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char			*dest;
	char			*src;
	unsigned int	n;

	dest = malloc(strlen(src) + 1);
	src = "blaublaublau";
	n = 8;
	printf("%s", ft_strncpy(dest, src, n));
}
